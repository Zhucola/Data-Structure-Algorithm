<?php
/*
 |--------------------------------------------------------------
 | 冒泡排序
 | 时间复杂度O(n^2)
 |--------------------------------------------------------------
*/

$arr = [6,7,12,34,54,11,23,87,54,43,1,2,9,6,160,123];
function BubbleSort($arr){
	for($x=0,$len=count($arr);$x<$len-1;$x++){
		for($y=0;$y<$len-$x-1;$y++){
			if($arr[$y]>$arr[$y+1]){
				$temp = $arr[$y];
				$arr[$y] = $arr[$y+1];
				$arr[$y+1] = $temp;
			}
		}
	}
	return $arr;
}
$res = BubbleSort($arr);
